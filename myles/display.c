// ---------------------------------------------------------------------------
// Text rendering
// ---------------------------------------------------------------------------

void display_draw_char(uint16_t x, uint16_t y, char c, uint16_t color, uint16_t bg) {
    // Clamp to printable ASCII range
    if (c < 0x20 || c > 0x7E) {
        c = 0x20; // render as space for out-of-range characters
    }

    const uint8_t *glyph = font[(uint8_t)c - 0x20];

    // Set a 6×8 window (5 font columns + 1 spacing column, 7 rows + 1 spacing row)
    display_set_window(x, y, x + 5, y + 7);

    uint8_t hi_fg = (uint8_t)(color >> 8);
    uint8_t lo_fg = (uint8_t)(color & 0xFF);
    uint8_t hi_bg = (uint8_t)(bg >> 8);
    uint8_t lo_bg = (uint8_t)(bg & 0xFF);

    gpio_put(TFT_DC_PIN, 1);   // DC high = data
    gpio_put(TFT_CS_PIN, 0);   // Hold CS low for entire burst

    // The window is filled row-by-row (top to bottom, left to right)
    for (uint8_t row = 0; row < 8; row++) {
        for (uint8_t col = 0; col < 6; col++) {
            uint8_t buf[2];
            if (col < 5 && row < 7 && (glyph[col] & (1 << row))) {
                buf[0] = hi_fg;
                buf[1] = lo_fg;
            } else {
                buf[0] = hi_bg;
                buf[1] = lo_bg;
            }
            spi_write_blocking(spi0, buf, 2);
        }
    }

    gpio_put(TFT_CS_PIN, 1);   // CS deassert
}

void display_draw_string(uint16_t x, uint16_t y, const char *str, uint16_t color, uint16_t bg) {
    while (*str) {
        display_draw_char(x, y, *str, color, bg);
        x += 6; // 5 font pixels + 1 spacing pixel
        str++;
    }
}
